void task_2 (int n, FILE* fp) {
	char* bits = "";

	while (n > 0) {
		asprintf (&bits, "%d%s", (n & 1), bits);
		n = n >> 1;
	}
	printf ("task_2 : %s\n", bits);
	fprintf (fp, "task_2 : %s\n", bits);
}

